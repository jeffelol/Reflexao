
package br.edu.unisep.jeferson;

public class Calculadora {

    public double adicao(float valor, float valor2){
        return (valor + valor);
    }
    
    public double subtracao(float valor, float valor2){
        return (valor - valor2);
    }
    
    public double divisao(float valor, float valor2){
        return (valor / valor2);
    }
    
    public double multiplicacao(float valor, float valor2){
        return (valor * valor2);
    }

}
